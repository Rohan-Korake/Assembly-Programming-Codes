DATA SEGMENT
BSTR DB "HELLO WORLD$"
ASTR DB 40 DUP('$')
MS1 DB 05,10, "BEFORE REVERSE : $"
MS2 DB 05,10, "AFTER REVERSE : $"
COUNT DB 0
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START : 
MOV AX,DATA
MOV DS,AX
LEA SI BSTR

NEXT : 
MOV AL, [SI]
CMP AL, '$' 
JE EXIT
INC SI
INC COUNT
JMP NEXT

EXIT : 
LEA DI, ASTR

UP : 
DEC SI
MOV AL, [SI]
MOV [DI],AL
INC DI
DEC COUNT
JNZ UP
MOV AH, 09H
LEA DX, MS1
INT 21H

MOV AH, 09H
LEA DX, BSTR
INT 21H

MOV AH, 09H
LEA DX, MS2
INT 21H

MOV AH, 09H
LEA DX, ASTR
INT 21H

MOV AH, 4CH
INT 21H
CODE ENDS
END START
