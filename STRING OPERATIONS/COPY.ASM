DATA SEGMENT
ORG_STR DB 'HELLO$'
COPY_STR DB 6 DUP('$')
MSG1 DB 10,13,'ORIGINAL STRING: $'
MSG2 DB 10,13,'COPIED STRING: $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX

MOV AH, 09H
LEA DX, MSG1
INT 21H

MOV AH, 09H
LEA DX, ORG_STR
INT 21H
LEA SI, ORG_STR
LEA DI, COPY_STR
MOV CX, 5
COPY_LOOP:
MOV AL, [SI]
MOV [DI], AL
INC SI
INC DI
LOOP COPY_LOOP
MOV BYTE PTR [DI], '$'
MOV AH, 09H
LEA DX, MSG2
INT 21H
MOV AH, 09H
LEA DX, COPY_STR
INT 21H

MOV AH, 4CH
INT 21H

CODE ENDS
END START
