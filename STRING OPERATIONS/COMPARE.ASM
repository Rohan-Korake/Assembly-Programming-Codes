DATA SEGMENT 
STR1 DB 'HELLO$'
STR2 DB 'ROHAN$'
MS1 DB 'BOTH ARE EQUAL$'
MS2 DB 'BOTH ARE NOT EQUAL$'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
LEA SI,STR1
LEA DI,STR2
COMPARE:
MOV AL,[SI]
MOV BL,[DI]
CMP AL,BL
JNE NOT_EQUAL
CMP AL,'$'
JE END_COMPARE
INC SI
INC DI
JMP COMPARE
END_COMPARE:
MOV AH,09H
MOV DX,OFFSET MS1
INT 21H
JMP EXIT
NOT_EQUAL:
MOV AH,09H
MOV DX,OFFSET MS2
INT 21H
EXIT:
MOV AH,4CH
INT 21H
CODE ENDS
END START