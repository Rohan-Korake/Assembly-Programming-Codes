DATA SEGMENT 
NUM1 DB 07H
NUM2 DB 02H
RES_ADD DB ?
RES_SUB DB ?
RES_MUL DB ?
RES_QUE DB ?
RES_REM DB ?
DATA ENDS 

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
CALL ADD_NUM
CALL SUB_NUM
CALL MUL_NUM
CALL DIV_NUM
INT 03H
ADD_NUM PROC
MOV AL,NUM1
MOV BL,NUM2
ADD AL,BL
MOV RES_ADD,AL
RET
ADD_NUM ENDP
SUB_NUM PROC
MOV AL,NUM1
MOV BL,NUM2
SUB AL,BL
MOV RES_SUB,AL
RET
SUB_NUM ENDP
MUL_NUM PROC
MOV AL,NUM1
MOV BL,NUM2
MUL BL
MOV RES_MUL,AL
RET
MUL_NUM ENDP
DIV_NUM PROC 
MOV AL,NUM1
MOV BL,NUM2
DIV BL
MOV RES_QUE,AL
MOV RES_REM,AH
RET 
DIV_NUM ENDP
CODE ENDS
END START

