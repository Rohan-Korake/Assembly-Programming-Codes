DATA SEGMENT
Array DB 40H, 20H, 70H, 10H, 60H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX
    MOV CL, 05H

LOOP1: 
    LEA SI, Array
    MOV CH, 04H

LOOP2: 
    MOV AX, [SI]
    MOV BX, [SI+1]
    CMP AX, BX
    JAE SKIP
    MOV [SI], BX
    MOV [SI+1], AX

SKIP: 
    INC SI
    DEC CH
    JNZ LOOP2
    DEC CL
    JNZ LOOP1

    MOV AH, 4CH
    INT 21H
CODE ENDS
END START
