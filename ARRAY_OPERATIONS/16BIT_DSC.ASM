DATA SEGMENT
Array DW 0040H, 0020H, 0070H, 0010H, 0060H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX
    MOV CL, 05H

LOOP1: 
    LEA SI, Array
    MOV CH, 04H

LOOP2: 
    MOV AX, [SI]
    MOV BX,[SI+2]
    CMP AX, BX
    JAE SKIP
    MOV [SI], BX
    MOV [SI+2], AX

SKIP:
    INC SI
    DEC CH
    JNZ LOOP2
    DEC CL
    JNZ LOOP1

    MOV AH, 4CH
    INT 21H
CODE ENDS
END START
