DATA SEGMENT 
NUM DW 0017H
MS1 DB 10,13,'EVEN NUMBER$'
MS2 DB 10,13,'ODD NUMBER$'
DATA ENDS
CODE SEGMENT 
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX
MOV BX, NUM
AND BX, 0001H
JZ EVEN
ODD:
MOV AH, 09H
MOV DX, OFFSET MS2
INT 21H
JMP EXIT
EVEN:
MOV AH, 09H
MOV DX, OFFSET MS1
INT 21H
EXIT:
MOV AH, 4CH
INT 21H
CODE ENDS
END START
