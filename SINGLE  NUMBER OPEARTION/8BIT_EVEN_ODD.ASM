DATA SEGMENT 
NUM DB 17H
MS1 DB 'EVEN NUMBER$'
MS2 DB 'ODD NUMBER$'
DATA ENDS

CODE SEGMENT 
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV AL,[NUM]
AND AL,01H
JZ EVEN
ODD:
MOV AH,09H
MOV DX,OFFSET MS2
INT 21H
JMP EXIT
EVEN:
MOV AH,09H
MOV DX,OFFSET MS1
INT 21H
EXIT:
MOV AH,4CH
INT 21H
CODE ENDS
END START