DATA SEGMENT 
A DB 01H
B DB 02H
C DB 03H
D DB 04H
SUM DB ?
Z DB ?
DATA ENDS

CODE SEGMENT 
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV AL,A
MOV BL,B
CALL ADD_BYTE
MOV SUM,AL 
MOV AL,C
MOV BL,D
CALL ADD_BYTE
MUL SUM 
MOV Z,AL 
ADD_BYTE PROC
ADD AL,BL 
RET 
ADD_BYTE ENDP
INT 03H
CODE ENDS
END START